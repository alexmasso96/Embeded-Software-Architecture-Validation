def test_find_hlt_files(tmp_path):
    (tmp_path / "ModelA_Test_Case_Design.md").write_text("x")
    (tmp_path / "ModelB_Test_Case_Design.md").write_text("y")
    (tmp_path / "rules.md").write_text("z")  # excluded
    found = ctx.find_hlt_files(str(tmp_path))
    assert [os.path.basename(f) for f in found] == [
        "ModelA_Test_Case_Design.md", "ModelB_Test_Case_Design.md"
    ]


def test_find_hlt_files_missing_dir():
    assert ctx.find_hlt_files("/no/such/dir") == []


def test_parse_hlt_file(tmp_path):
    p = tmp_path / "ModelA_Test_Case_Design.md"
    p.write_text(SAMPLE_HLT, encoding="utf-8")
    parsed = ctx.parse_hlt_file(str(p))
    assert parsed["model_name"] == "ModelA"
    assert len(parsed["test_cases"]) == 2
    tc0 = parsed["test_cases"][0]
    assert tc0["title"] == "TC.001 - Temperature read"
    assert tc0["id"] == "0:TC.001 - Temperature read"
    assert "Given the ECU is running" in tc0["raw"]
    # Placeholder-only -> not yet generated.
    assert tc0["has_lowlevel"] is False


def test_parse_hlt_detects_existing_lowlevel(tmp_path):
    filled = SAMPLE_HLT.replace(
        "*(Paste the low-level test cases generated by GitHub Copilot here)*",
        "1. Set breakpoint...\n2. Run.", 1)
    p = tmp_path / "M_Test_Case_Design.md"
    p.write_text(filled, encoding="utf-8")
    parsed = ctx.parse_hlt_file(str(p))
    assert parsed["test_cases"][0]["has_lowlevel"] is True
    assert parsed["test_cases"][1]["has_lowlevel"] is False


