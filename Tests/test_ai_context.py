def test_build_source_context_scores_and_includes(tmp_path):
    (tmp_path / "temp.c").write_text(
        "void IfTemp_read(void){ int temperature = 5; }", encoding="utf-8")
    (tmp_path / "unrelated.c").write_text(
        "void foo(void){ int bar = 0; }", encoding="utf-8")
    out = ctx.build_source_context(str(tmp_path), ["IfTemp temperature read"], budget_chars=5000)
    assert "temp.c" in out
    assert "IfTemp_read" in out


def test_build_source_context_empty_when_no_source():
    assert ctx.build_source_context("", ["x"]) == ""
    assert ctx.build_source_context("/no/such/path", ["x"]) == ""


def test_build_source_context_respects_budget(tmp_path):
    big = "int x; // keyword padding\n" * 5000
    (tmp_path / "big.c").write_text("keyword\n" + big, encoding="utf-8")
    out = ctx.build_source_context(str(tmp_path), ["keyword"], budget_chars=1000)
    assert len(out) <= 1300  # header + truncated body, bounded


def test_extract_keywords_filters_stopwords_and_short():
    kws = ctx.extract_keywords(["The test shall verify temperature reading"])
    assert "temperature" in kws
    assert "the" not in kws and "test" not in kws  # stopwords
    assert all(len(k) >= 3 for k in kws)


