def test_write_lowlevel_output(tmp_path):
    parsed = ctx.parse_hlt_file_from_text(SAMPLE_HLT) if hasattr(ctx, "parse_hlt_file_from_text") else None
    # parse via file (no text helper) for portability
    p = tmp_path / "ModelA_Test_Case_Design.md"
    p.write_text(SAMPLE_HLT, encoding="utf-8")
    parsed = ctx.parse_hlt_file(str(p))
    cases = parsed["test_cases"]
    generated = {cases[0]["id"]: "1. Set breakpoint in IfTemp_read.\n2. Run."}
    out_path = ctx.write_lowlevel_output(
        str(tmp_path), parsed["model_name"], parsed["title"], cases, generated)
    assert os.path.basename(out_path) == "ModelA_LowLevel.md"
    content = open(out_path, encoding="utf-8").read()
    assert "Low-Level Test Case Design - ModelA" in content
    assert "Set breakpoint in IfTemp_read" in content
    # Only the generated case is included.
    assert "TC.001" in content
    assert "TC.002" not in content
    # Placeholder text must not leak through.
    assert "Paste the low-level" not in content


def test_clean_generated_strips_echoed_header():
    assert ctx.clean_generated("### Low Level Test Case Design\n\n1. Step") == "1. Step"
    assert ctx.clean_generated("1. Step") == "1. Step"
    assert ctx.clean_generated("```markdown\n1. Step\n```") == "1. Step"


def test_write_lowlevel_no_duplicate_header(tmp_path):
    p = tmp_path / "ModelA_Test_Case_Design.md"
    p.write_text(SAMPLE_HLT, encoding="utf-8")
    parsed = ctx.parse_hlt_file(str(p))
    cases = parsed["test_cases"]
    # Model echoes the header back, as real models do.
    gen = {cases[0]["id"]: "### Low Level Test Case Design\n\n1. Set breakpoint."}
    out_path = ctx.write_lowlevel_output(
        str(tmp_path), parsed["model_name"], parsed["title"], cases, gen)
    content = open(out_path, encoding="utf-8").read()
    assert content.count(ctx.LOWLEVEL_HEADER) == 1  # exactly one, no duplicate


def test_apply_lowlevel_no_duplicate_header(tmp_path):
    p = tmp_path / "M_Test_Case_Design.md"
    p.write_text(SAMPLE_HLT, encoding="utf-8")
    parsed = ctx.parse_hlt_file(str(p))
    gen = {parsed["test_cases"][0]["id"]:
           "### Low Level Test Case Design\n\n1. Step."}
    ctx.apply_lowlevel_to_hlt(str(p), gen)
    text = p.read_text(encoding="utf-8")
    # First case: one header (the original), placeholder replaced by steps only.
    assert "### Low Level Test Case Design\n\n### Low Level" not in text
    assert "1. Step." in text


def test_apply_lowlevel_to_hlt_fills_placeholder(tmp_path):
    p = tmp_path / "ModelA_Test_Case_Design.md"
    p.write_text(SAMPLE_HLT, encoding="utf-8")
    parsed = ctx.parse_hlt_file(str(p))
    cases = parsed["test_cases"]
    generated = {cases[0]["id"]: "1. Set breakpoint.\n2. Run."}
    n = ctx.apply_lowlevel_to_hlt(str(p), generated)
    assert n == 1
    text = p.read_text(encoding="utf-8")
    assert "1. Set breakpoint." in text
    # Case 0 placeholder gone; case 1 placeholder remains.
    assert text.count("Paste the low-level") == 1
    # The high-level structure is preserved.
    assert "## Test Case: TC.001 - Temperature read" in text
    assert "## Test Case: TC.002 - Speed read" in text


def test_apply_lowlevel_idempotent(tmp_path):
    p = tmp_path / "M_Test_Case_Design.md"
    p.write_text(SAMPLE_HLT, encoding="utf-8")
    parsed = ctx.parse_hlt_file(str(p))
    gen = {parsed["test_cases"][0]["id"]: "STEPS"}
    assert ctx.apply_lowlevel_to_hlt(str(p), gen) == 1
    # Second run: placeholder already filled -> nothing changes.
    assert ctx.apply_lowlevel_to_hlt(str(p), gen) == 0
    assert p.read_text(encoding="utf-8").count("STEPS") == 1


def test_apply_lowlevel_no_match(tmp_path):
    p = tmp_path / "M_Test_Case_Design.md"
    p.write_text(SAMPLE_HLT, encoding="utf-8")
    before = p.read_text(encoding="utf-8")
    assert ctx.apply_lowlevel_to_hlt(str(p), {"99:nope": "x"}) == 0
    assert p.read_text(encoding="utf-8") == before


