def test_defaults_when_no_db():
    assert ctx.get_prompt(None) == ctx.DEFAULT_PROMPT
    assert ctx.get_rules(None) == ctx.DEFAULT_RULES


def test_default_prompt_is_deroleplayed():
    # The removed roleplay opener must not be present in the default prompt.
    assert "You are a specialized" not in ctx.DEFAULT_PROMPT
    assert ctx.DEFAULT_PROMPT.startswith("Your task is to generate")


def test_db_roundtrip_prompt_rules():
    db = FakeDB()
    # Falls back to defaults before anything is stored.
    assert ctx.get_prompt(db) == ctx.DEFAULT_PROMPT
    ctx.set_prompt(db, "custom prompt")
    ctx.set_rules(db, "custom rules")
    assert ctx.get_prompt(db) == "custom prompt"
    assert ctx.get_rules(db) == "custom rules"


def test_closed_db_uses_defaults():
    db = FakeDB(open_=False)
    ctx.set_prompt(db, "ignored")  # no-op when closed
    assert ctx.get_prompt(db) == ctx.DEFAULT_PROMPT


