class FakeDB:
    """Minimal stand-in for ProjectDatabase meta storage."""
    def __init__(self, open_=True):
        self.is_open = open_
        self._meta = {}

    def get_meta(self, key, default=None):
        return self._meta.get(key, default)

    def set_meta(self, key, value):
        self._meta[key] = value


SAMPLE_HLT = """# Test Case Design - ModelA

This document contains the generated test cases for the **ModelA** architecture model.

---
## Test Case: TC.001 - Temperature read

Given the ECU is running
When IfTemp is read
Then the value is valid

### Low Level Test Case Design
*(Paste the low-level test cases generated by GitHub Copilot here)*

---
## Test Case: TC.002 - Speed read

Given the ECU is running
When IfSpeed is read
Then the value is valid

### Low Level Test Case Design
*(Paste the low-level test cases generated by GitHub Copilot here)*
"""


